int		ft_atoi(const char *str)
{
	int		sign;
	int		num;

	sign = 1;
	num = 0;
	while (*str == '\t' || *str == '\n' || *str == ' ' || *str == '\v'
			|| *str == '\r' || *str == '\f')
		str++;
	if (*str == '-')
		sign = -1;
	if (*str == '-' || *str == '+')
		str++;
	while (*str >= '0' && *str <= '9')
	{
		num = num * 10 + (*str - '0');
		str++;
	}
	return (num * sign);
}
